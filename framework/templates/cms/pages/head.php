<p>
    Pages are simply a collection of templates. <br>
    Special pages - such as the index, events and sermons page - are not available through this feature. Those templates must be updated individually or through a special editor. <br>
    <br>
    To create a page, simple type the name of the page you would like to create. <br>
    You will need to add templates to that page. The standard order and naming convention is: <br>
    <ul>
        <li>Page name: example</li>
        <li>headtop - shared between all pages</li>
        <li>headexample - page specific css and js</li>
        <li>headbot - shared between all pages</li>
        <li>example - page body</li>
        <li>footer - shared between all pages</li>
    </ul>
    <br>
    In order to create or delete a template, you must go to the "Edit Templates" page. <br>
    After creating a template there, you can add it to your page using this page. <br>
    Deleting a page or removing a template from a page will not delete the templates. <br>
</p>
